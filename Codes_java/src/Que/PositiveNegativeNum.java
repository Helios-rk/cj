package Que;

public class PositiveNegativeNum {

	public static void main(String[] args) {
		int num = 0;
		if (num > 0) {
			System.out.println("number is positive");
		} else if (num < 0) {
			System.out.println("number is negative");
		} else {
			System.out.println("number is neutral or zero");
		}
	}
}